#!/bin/bash

sed -n "/^\/tmp\/xcode\/[^\/]*\/${1}\//,\$p" |
sed -e "s/^.*\/${1}\/[^\.]*\.swift$//" \
    -e "s/^.*\/${1}\/[^\.]*\.m$//" \
    -e "s/^.*\/${1}\/\([^\.]*\.swift\)/\1/" \
    -e "s/^.*\/${1}\/\([^\.]*\.m\)/\1/" \
    -e "/^\*\* BUILD FAILED \*\*/,\$d" |
grep -v ${1} |
grep -v 'cd /tmp/xcode'
